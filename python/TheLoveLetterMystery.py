# Complete the theLoveLetterMystery function below.
def theLoveLetterMystery(s):
    count = 0
    reverse_s = s[::-1]
            
    for i in range(int(len(s)/2)):
        count += abs(ord(reverse_s[i]) - ord(s[i]))
        
    return count
    
